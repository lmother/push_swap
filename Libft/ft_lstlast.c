#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*tmp;

	tmp = lst;
	if (tmp != NULL)
	{
		while (tmp->next != 0)
			tmp = tmp->next;
	}
	return (tmp);
}
